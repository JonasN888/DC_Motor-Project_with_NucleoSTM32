/**
 * \fn void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
 * \brief Fonction pour calculer la vitesse de rotation du moteur. Cette interruption est appelée 1000 fois par seconde
 *
 * \param TIM_HandleTypeDef *htim
 * \return Ne renvoie rien
 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	 //On va brancher l'encodeur sur PB3 et PB0 de notre carte
	 /*On a 1024 positions pour un tour. Comme on compte pour chaque front montant et chaque front descendant,
	  on va mesurer au total 4096 positions.
	  Il faut donc diviser la position par 4096 pour incrémenter une fois par rotation complète.
	  On fait la conversion en vitesse en calibrant le Timer4 de telle manière qu'il génère une interruption chaques
	  milliseconde. Il faut donc multiplier l'écart entre la position par 1000 pour avoir la vitesse de rotation en seconde.
	  D'abord on teste si l'interruption vient du Timer4*/
	if (&htim == &htim4){
		  position = TIM2->CNT;
		  position = (int16_t) position/4096;
		  speed = 1000*(position - old_position);
		  old_position = position;
	}
}


